package workshop4;

public class Calculator {
	/**
	 * This method sums the value of two parameters passed
	 * @param a is the parameter to add
	 * @param b is the parameter to add
	 * @return It returns the value of sum
	 */
	//sum
	public int sum(int a, int b) {
		return a + b;
	}
	/**
	 * This method subtracts the value of two parameters passed
	 * @param a
	 * @param b
	 * @return It returns the value of subtraction
	 */
	public int subtract(int a, int b) {
		return a - b;
	}
	/**
	 * This method multiplies the value of two parameters passed
	 * @param a
	 * @param b
	 * @return It returns the value of multiplication
	 */
	public int multiply(int a, int b) {
		return a * b;
	}

}
